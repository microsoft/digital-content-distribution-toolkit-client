package com.microsoft.mobile.polymer.mishtu.storage.snappyDB;

public class LocalStorageException extends Exception
{
    public LocalStorageException()
    {
        super();
    }

    public LocalStorageException(String message)
    {
        super(message);
    }

    public LocalStorageException(Throwable throwable)
    {
        super(throwable);
    }

    public LocalStorageException(String message, Throwable throwable)
    {
        super(message, throwable);
    }
}
